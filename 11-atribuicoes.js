//Incremento e decremento com valor especifico
//Operadores de atribuição aritimética
var x = 10;
var y = 5;


console.log("---------------------");
console.log("x =", x, "| y =", y);

x += y; // x mais y
console.log("x =", x, "| y =", y);

x -= y; // x menos y
console.log("x =", x, "| y =", y);

x *= y; // x vezes y
console.log("x =", x, "| y =", y);

x /= y; // x dividido y
console.log("x =", x, "| y =", y);

x **= y; // x elevado a y
console.log("x =", x, "| y =", y);

x %= y; // resto da divisão inteira de x por y
console.log("x =", x, "| y =", y);

x = 13;
x %= y; // resto da divisão inteira de x por y
console.log("x =", x, "| y =", y);

console.log("---------------------");